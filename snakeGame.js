function getRandomCoordinates() {
  var x, y;
  do {
    x = Math.floor(Math.random() * (canvas.width / gridSize)) * gridSize;
    y = Math.floor(Math.random() * (canvas.height / gridSize)) * gridSize;
  } while (labyrinth[Math.floor(y / gridSize)][Math.floor(x / gridSize)] === 1);
  return { x: x, y: y };
}



var food = getRandomCoordinates();


function drawSnake() {
  ctx.fillStyle = "green";
  for (var i = 0; i < snake.length; i++) {
    ctx.fillRect(snake[i].x, snake[i].y, gridSize, gridSize);
  }
}


function drawFood() {
  ctx.fillStyle = "red";
  ctx.fillRect(food.x, food.y, gridSize, gridSize);
}


function gameLogic() {

  var newHead = { x: snake[0].x, y: snake[0].y };
  switch (direction) {
    case 'up':
      newHead.y -= gridSize;
      break;
    case 'down':
      newHead.y += gridSize;
      break;
    case 'left':
      newHead.x -= gridSize;
      break;
    case 'right':
      newHead.x += gridSize;
      break;
  }

  var headRow = Math.floor(newHead.y / gridSize);
  var headCol = Math.floor(newHead.x / gridSize);

  if (labyrinth[headRow][headCol] === 1) {

    gameOver();
    return;
  }

  
  if (
    newHead.x < 0 ||
    newHead.x >= canvas.width ||
    newHead.y < 0 ||
    newHead.y >= canvas.height
  ) {
    gameOver();
    return;
  }

  
  for (var i = 1; i < snake.length; i++) {
    if (newHead.x === snake[i].x && newHead.y === snake[i].y) {
      gameOver();
      return;
    }
  }

  
  if (
    newHead.x < food.x + gridSize &&
    newHead.x + gridSize > food.x &&
    newHead.y < food.y + gridSize &&
    newHead.y + gridSize > food.y
  ) {
    
    snakeSize++;
    food = getRandomCoordinates();
  }

  snake.unshift(newHead);
  
  if (snake.length > snakeSize) {
    snake.pop();
  }
}

function gameOver() {
  alert('Гра закінчена! Очки: ' + snakeSize);
 
  snake = [{ x: 10, y: 50 }];
  snakeSize = 1;
  direction = 'right';
  food = getRandomCoordinates();
}


document.addEventListener('keydown', function (event) {
  switch (event.key) {
    case 'ArrowUp':
      if (direction !== 'down') {
        direction = 'up';
      }
      break;
    case 'ArrowDown':
      if (direction !== 'up') {
        direction = 'down';
      }
      break;
    case 'ArrowLeft':
      if (direction !== 'right') {
        direction = 'left';
      }
      break;
    case 'ArrowRight':
      if (direction !== 'left') {
        direction = 'right';
      }
      break;
  }
});


function drawLabyrinth() {
  for (var i = 0; i < labyrinth.length; i++) {
    for (var j = 0; j < labyrinth[i].length; j++) {
      if (labyrinth[i][j] === 1) {
        ctx.fillStyle = "gray"; 
      } else {
        ctx.fillStyle = "white";
      }
      ctx.fillRect(j * gridSize, i * gridSize, gridSize, gridSize);
    }
  }
}


function gameLoop() {
  ctx.clearRect(0, 0, canvas.width, canvas.height);


  drawLabyrinth();
  gameLogic();
  drawSnake();
  drawFood();

  setTimeout(function () {
    requestAnimationFrame(gameLoop);
  }, gameInterval);
}

gameLoop();
